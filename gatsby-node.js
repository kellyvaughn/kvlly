const path = require('path')

exports.createPages = ({ actions, graphql }) => {
  const { createPage } = actions
  return graphql(`
    {
      allShopifyProduct {
        edges {
          node {
            handle
          }
        }
      }
    }
  `).then(result => {
    result.data.allShopifyProduct.edges.forEach(({ node }) => {
      createPage({
        path: `/shop/${node.handle}`,
        component: path.resolve(`./src/templates/product.js`),
        context: {
          handle: node.handle,
        },
      })
    })
  })
}
